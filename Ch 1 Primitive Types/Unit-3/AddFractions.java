public class AddFractions 
{
    public static void main(String[] args)
    {
  int numFir = 1;
  int numSec = 2;
  int denFir = 2;
  int denSec = 5;
  
  
  System.out.println("The numerator of the first fraction is "+"1");
  System.out.println("The denominator of the first fraction is "+"2");
  System.out.println("The numerator of the second fraction is "+"2");
  System.out.println("The denominator of the second fraction is"+"5");
  System.out.print("The sum of ");
  
  
  System.out.print((numFir + "/" + denFir) + " + ");
  System.out.print((numSec + "/" + denSec) + " = ");
  System.out.print((numFir + "/" + denFir) + (numSec + denSec));
    
        
    }
}